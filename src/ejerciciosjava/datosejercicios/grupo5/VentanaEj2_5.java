    private void guardarDirectBOTONActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarDirectBOTONActionPerformed
        String texto = textoTXTAREA.getText();
        File rutaArch = new File(pathTXTFIELD.getText());
        String nombreArch = fileNameTXTFIELD.getText();
        File archivo = new File(rutaArch, nombreArch);
        if (!rutaArch.exists()) {
            System.out.println("La ruta no existe. La creamos");
            rutaArch.mkdirs();
        }
        if (!archivo.exists()) {
            System.out.println("El archivo no existe. Lo creamos");
            try {
                rutaArch.createNewFile();
            } catch (IOException ex) {
                Logger.getLogger(VentanaEj2_5.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

        //Abrimos fichero para escritura
        FileWriter archivoWrite = null;
        PrintWriter pw = null;
        System.out.println(modoGrabarCOMBOBOX.getSelectedIndex());
        try {

            if (modoGrabarCOMBOBOX.getSelectedIndex() == 0) {
                archivoWrite = new FileWriter(archivo, true);
                pw = new PrintWriter(archivoWrite);
            } else {
                archivoWrite = new FileWriter(archivo);
                pw = new PrintWriter(archivoWrite);
            }
            System.out.println(texto);
            pw.print(texto);
        } catch (IOException ex) {
            Logger.getLogger(VentanaEj2_5.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                //  aprovechamos el finally para asegurarnos que se cierra el fichero.
                if (null != archivoWrite) {
                    archivoWrite.close();
                }
            } catch (IOException e2) {
                System.out.println(e2.getMessage());
            }
        }

    }//GEN-LAST:event_guardarDirectBOTONActionPerformed

    private void leerBOTONActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_leerBOTONActionPerformed
        String texto = textoTXTAREA.getText();
        File rutaArch = new File(pathTXTFIELD.getText());
        String nombreArch = fileNameTXTFIELD.getText();
        File archivo = new File(rutaArch, nombreArch);
        FileReader archivoRead = null;
        BufferedReader bufferLectura;
        try {
            archivoRead = new FileReader(archivo);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(VentanaEj2_5.class.getName()).log(Level.SEVERE, null, ex);
        }

        if (archivo.exists()) {
            if (archivo.canRead()) {
                bufferLectura = new BufferedReader(archivoRead);
                String linea;
                textoTXTAREA.setText("");
                try {
                    while ((linea = bufferLectura.readLine()) != null) {
                        System.out.println(linea);
                        textoTXTAREA.append(linea + "\n");
                    }
                } catch (IOException ex) {
                    Logger.getLogger(VentanaEj2_5.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                System.out.println("no tiene permiso para leer ese archivo");
            }

        } else {
            System.out.println("Ese archivo no existe");
        }
    }//GEN-LAST:event_leerBOTONActionPerformed

