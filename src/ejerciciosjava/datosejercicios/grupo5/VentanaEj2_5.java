    /**
     * Halla la ruta relativa a una carpeta
     * @param file File Archivo de referencia
     * @param folder carpeta relativa
     */    
    public static String getRelativePath(File file, File folder) {
        String filePath = file.getAbsolutePath();
        int fileName = file.getName().length();
        String folderPath = folder.getAbsolutePath();
        String relativePath;

        if (filePath.startsWith(folderPath)) {
            //borra la ruta hasta la carpeta de referencia
            relativePath = filePath.substring(folderPath.length() + 1);
            //borra el nombre del archivo
            relativePath = relativePath.substring(0, relativePath.length() - fileName);
            System.out.println("Ruta relativa: "+ relativePath);
            return relativePath;
        } else {
            return null;
        }
    }

