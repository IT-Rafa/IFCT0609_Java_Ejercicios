    private void enviarFormulario() {
        java.awt.EventQueue.invokeLater(new Runnable() {
            public void run() {
                boolean casado;
                boolean[] lenguages = {false, false, false, false};

                if (casadoRadio.isSelected()) {
                    casado = true;
                } else {
                    casado = false;
                }
                if (javaCheckBox.isSelected()) {
                    lenguages[0] = true;
                }
                if (cCheckBox.isSelected()) {
                    lenguages[1] = true;
                }
                if (csharpCheckBox.isSelected()) {
                    lenguages[2] = true;
                }
                if (javascriptCheckBox.isSelected()) {
                    lenguages[3] = true;
                }

                new VentanaEj1_5_mostrar(
                        nombreTxtF.getText(),
                        apellTxtF.getText(),
                        dniTxtF.getText(),
                        estudiosCombo.getSelectedItem().toString(),
                        Integer.parseInt(edadJSpinner.getValue().toString()),
                        casado,
                        lenguages
                ).setVisible(true);

            }
        });

    }

    private void guardarFormulario() {
        System.out.println("\nGUARDANDO DATOS EN ARCHIVO");
        //CREAR CARPETA
        //Definimos directorio donde queremos los datos
        String nombreArchivo;
        File carpeta;
        carpeta = new File("TextosCreados");

        //Comprobamos si existe esa carpeta y si no la creamos
        if (carpeta.exists()) {
            System.out.println("Carpeta ya existe");
        } else {
            //intentamos crearla
            if (carpeta.mkdirs()) {
                System.out.println("Carpeta " + carpeta.getName() + " creada.");
            }else{
                System.out.println("Problema al crear carpeta.");
            }
        }
        //CREAR ARCHIVO
        //Definimos nombre del archivo a crear
        nombreArchivo= "formulario.txt";
        //Definimos ruta con nombre del archivo
        File archivo=new File(carpeta,nombreArchivo);
        
        
    }

